    private void cSet3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cSet3ActionPerformed
        // TODO add your handling code here:
        String text = tManual3.getText();
        boolean success;
        
        try
        {
            double newVal = Double.parseDouble(text);
            if (m_ShowLatLonInDegrees)
                newVal *= MathConstants.DEG2RAD;
            m_ChangeListener.setLoiterLatRad (newVal);
            success = true;
        }
        catch (Exception e)
        {
            success = false;
        }
        
        if (success)
        {
            tManual3.setText("");
        }
        else
        {
            JOptionPane.showConfirmDialog(this, "Invalid value set: \'" + text + "\'", "Error!", JOptionPane.OK_CANCEL_OPTION, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cSet3ActionPerformed

